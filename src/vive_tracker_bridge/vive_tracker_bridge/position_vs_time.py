from io import StringIO
df = pd.read_csv(StringIO(data))

# Force numeric
for col in ['timestamp_sec', 'x_m', 'y_m', 'z_m']:
    df[col] = pd.to_numeric(df[col], errors='coerce')

df = df.dropna(subset=['x_m', 'z_m'])

x = df["x_m"].to_numpy()
z = df["z_m"].to_numpy()
t = df["timestamp_sec"].to_numpy()

# Normalize to start at (0,0)
x_start = x[0]
z_start = z[0]
x_rel = x - x_start
z_rel = z - z_start

# Forward axis = z_rel (main movement)
TOLERANCE = 0.05

# Ideal line from (0,0) to end
z_end_rel = z_rel[-1]
x_end_rel = x_rel[-1]
dz = z_end_rel - 0
dx = x_end_rel - 0
straight_distance = np.hypot(dx, dz)

ux = dx / straight_distance if straight_distance > 0 else 0
uz = dz / straight_distance if straight_distance > 0 else 0

projection = x_rel * ux + z_rel * uz
ideal_x = projection * ux
ideal_z = projection * uz

perp_x, perp_z = -uz, ux

upper_x = ideal_x + perp_x * TOLERANCE
upper_z = ideal_z + perp_z * TOLERANCE
lower_x = ideal_x - perp_x * TOLERANCE
lower_z = ideal_z - perp_z * TOLERANCE

lateral_error = (x_rel - ideal_x) * perp_x + (z_rel - ideal_z) * perp_z
abs_lateral_error = np.abs(lateral_error)

final_error = abs_lateral_error[-1]
max_drift = np.max(abs_lateral_error)
precision_rms = np.sqrt(np.mean(lateral_error ** 2))

# Cumulative path length
deltas = np.sqrt(np.diff(x_rel)**2 + np.diff(z_rel)**2)
total_path_length = np.sum(deltas)

print("\n=== ROBOT MOVEMENT SUMMARY ===")
print(f"Net forward displacement (z): {straight_distance:.3f} m")
print(f"Total path length: {total_path_length:.3f} m")
print(f"Max lateral drift (x): {max_drift:.3f} m")
print(f"Final lateral error: {final_error:.3f} m")
print(f"Precision RMS: {precision_rms:.3f} m")
print("Within ±5 cm corridor:", "Yes" if max_drift <= TOLERANCE else "No")
print("=================================\n")

# Plot: lateral (x) vs forward (z)
plt.figure(figsize=(10, 8))
plt.plot(z_rel, x_rel, 'b-', linewidth=2.5, label='Actual path')
plt.plot(ideal_z, ideal_x, 'k--', linewidth=1.5, label='Ideal straight line')
plt.plot(upper_z, upper_x, 'g--', label=f'+{TOLERANCE*100:.0f} cm')
plt.plot(lower_z, lower_x, 'g--', label=f'-{TOLERANCE*100:.0f} cm')

plt.scatter(0, 0, c='lime', s=150, edgecolors='k', zorder=10, label='Start (0,0)')
plt.scatter(z_end_rel, x_end_rel, c='red', s=150, edgecolors='k', zorder=10, label='End')

plt.xlabel("Forward / Backward (z relative, meters)")
plt.ylabel("Left / Right drift (x relative, meters)")
plt.title("2D Top-View Robot Movement (Vive Tracker)\nNormalized to start at (0,0)")
plt.legend(loc='upper left', bbox_to_anchor=(1.02, 1))
plt.axis("equal")
